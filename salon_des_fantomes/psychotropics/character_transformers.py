import random

def light_lucience(character):
	lightwords = ["a birthright thousandfold candle","a torch made of reverse prisims pristine","light within the locket of light","all my eyes reflecting that word that is the only word, coterminous with truth's energy: light!"]
	character.words.append(random.choice(lightwords))
	character.ideas.append("I alone can see this eternal and uncompromising light by which we all see")

